
// Var keyword
var ayush = 10 // initialization as well as assigning the value
var ayush = "Ayush"
console.log(ayush)
// Using var we could recreate as well as reassign variables with the same name
// Let keyword
let krish = "krish"
krish = 10
console.log(krish)

// Using let keyword we could create a variable which can be created only once in a scope and can be reassigned

const aarav = "Aarav"
aarav = 10



// Var - Can reassign and redeclare
// Let - can reassign but cannot redeclare
// const - cannot reassign and cannot redeclare